    private void cbcourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbcourseActionPerformed
        // TODO add your handling code here:
        int i=0;
        if(cbcourse.getSelectedIndex()>0)
        {
            try
            {
                rst=st.executeQuery("select * from StudReg");
                while(rst.next())
                    i++;
            }catch(SQLException ex){ex.printStackTrace();}
            tfregid.setText(tfacyear.getText()+"/"+cbcourse.getSelectedItem()+"/"+(i+1));
        }
    }//GEN-LAST:event_cbcourseActionPerformed

