    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(tfacyear.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "AC Year is Empty");
        else if(tfregid.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Registration ID is Empty");
        else if(tfsname.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Student Name is Empty");
        else if(tfdor.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Date Of Registration is Empty");
        else if(tfscontno.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Student Contact No is Empty");
        else if(tfpcontno.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Parent Contact No is Empty");
        else if(taddress.getText().isEmpty())
            JOptionPane.showMessageDialog(this, "Address is Empty");
        else
        {
            try
        {
 st.executeUpdate("Insert into StudReg values('"+tfregid.getText()+"','"+cbcourse.getSelectedItem()+"','"+tfsname.getText()+"','"+tfdor.getText()+"','"+tfscontno.getText()+"','"+tfpcontno.getText()+"','"+taddress.getText()+"','REG','"+tfacyear.getText()+"')");
 JOptionPane.showMessageDialog(this,"Registration Successful");
 dispose();
         }catch(SQLException ex){ex.printStackTrace();}
    }
      
    }//GEN-LAST:event_jButton1ActionPerformed

